        /// <summary>
        /// 获取实体中属性的名称
        /// </summary>
        /// <typeparam name="T"></typeparam>
        /// <param name="Attr">属性名</param>
        /// <returns></returns>
        public static string getAttrName<T>(Expression<Func<T>> Attr)
        {
            if (Attr == null)
            {
                throw new ArgumentNullException("Attr属性名为Null");
            }
            var memberExpression = Attr.Body as MemberExpression;
            if (memberExpression == null)
            {
                throw new ArgumentException("memberExpression为Null", "propertyExpression");
            }
            var property = memberExpression.Member as PropertyInfo;
            if (property == null)
            {
                throw new ArgumentException("property为Null", "propertyExpression");
            }
            var getMethod = property.GetGetMethod(true);
            if (getMethod.IsStatic)
            {
                throw new ArgumentException("该实体属于静态类,无法通过后面的代码", "propertyExpression");
            }
            return memberExpression.Member.Name;
        }

